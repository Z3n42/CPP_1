char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	pos;

	i = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	pos = i;
	i = 0;
	while (src[i] != '\0')
	{
		dest[pos] = src[i];
		pos++;
		i++;
	}
	dest[pos] = '\0';
	return (dest);
}
