char	*ft_strupcase(char *str)
{
	int	pos;

	pos = 0;
	while (str[pos] != '\0')
	{
		if (str[pos] >= 97 && str[pos] <= 122)
		{	
			str[pos] = (str[pos] - 32);
		}
		pos++;
	}
	return (str);
}
