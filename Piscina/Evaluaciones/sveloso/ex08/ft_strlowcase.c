char	*ft_strlowcase(char *str)
{
	int	pos;

	pos = 0;
	while (str[pos] != '\0')
	{
		if (str[pos] >= 65 && str[pos] <= 90)
		{	
			str[pos] = (str[pos] + 32);
		}
		pos++;
	}
	return (str);
}
