int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	value;

	value = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			value = 1;
		else
			return (0);
		i++;
	}
	if (str[0] == '\0')
		return (1);
	return (1);
}
